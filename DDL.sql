    COLUMN_NAME AS "Column",
    CASE WHEN NULLABLE = 'N' THEN 'NOT NULL' ELSE '' END AS "Nullable",
    DATA_TYPE || 
    CASE 
        WHEN DATA_TYPE IN ('CHAR', 'VARCHAR2') THEN '(' || DATA_LENGTH || ')'
        WHEN DATA_TYPE = 'NUMBER' AND DATA_PRECISION IS NOT NULL AND DATA_SCALE IS NOT NULL 
            THEN '(' || DATA_PRECISION || ',' || DATA_SCALE || ')'
        WHEN DATA_TYPE = 'NUMBER' AND DATA_PRECISION IS NOT NULL AND DATA_SCALE = 0 
            THEN '(' || DATA_PRECISION || ')'
        ELSE ''
    END AS "Data Type",
    COLUMN_ID AS "Order"
FROM USER_TAB_COLUMNS
WHERE TABLE_NAME = 'MENUITEM'
